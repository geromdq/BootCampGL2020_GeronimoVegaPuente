package encapsulamiento;

public class Gato {
	
	private String name;
	private int patas;
	
	public String getname() {
		return name;
	}
	
	public int getPatas() {
		return patas;
	}
	
	public void setname(String nuevoname) {		
		name = nuevoname;		
	}
	
	public void setPatas(int numeroPatas) {
		patas = numeroPatas;
	}
		

}
